import org.antlr.v4.runtime.tree.AbstractParseTreeVisitor;

import java.util.ArrayList;

/**
 * This class provides an empty implementation of {@link BigraphVisitor},
 * which can be extended to create a visitor which only needs to handle a subset
 * of the available methods.
 *
 * @param <T> The return type of the visit operation. Use {@link Void} for
 * operations with no return type.
 */
public class BigraphBaseVisitor<T> extends AbstractParseTreeVisitor<T> implements BigraphVisitor<T> {

	// We store identifiers in order to check repetitions and wrong uses
	private ArrayList<String> controls = new ArrayList<>();
	int controlsRepetitions = 0;
	private ArrayList<String> names    = new ArrayList<>();

	// This flag is set to true once we come across the 'model' rule
	// This way we can enforce check on model-derived 'expression' rules
	boolean modelVisited = false;

	@Override public T visitBigraph(BigraphParser.BigraphContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitControls(BigraphParser.ControlsContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitControl_statements(BigraphParser.Control_statementsContext ctx) {

		// Controls are added to the respective list
		// If the control name is already present an error is signaled
		String controlsIdentifier = ctx.getChild(1).toString();
		if(!controls.contains(controlsIdentifier))
			controls.add(controlsIdentifier);
		return visitChildren(ctx);

	}
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitNames(BigraphParser.NamesContext ctx) {return visitChildren(ctx);}
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitName_statements(BigraphParser.Name_statementsContext ctx) {

		// Names are added to the respective list
		// If names are already present an error is signaled
		String nameIdentifier = ctx.getChild(1).toString();
		if(!names.contains(nameIdentifier))
			names.add(nameIdentifier);
		return visitChildren(ctx);

	}
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitReactions(BigraphParser.ReactionsContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitReaction_statement(BigraphParser.Reaction_statementContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitExpression(BigraphParser.ExpressionContext ctx) {
		return visitChildren(ctx);
	}
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitRegions(BigraphParser.RegionsContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitPrefix(BigraphParser.PrefixContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitLinks(BigraphParser.LinksContext ctx) {
		if(modelVisited && ctx.getChild(0).toString().equals("@"))
			System.out.println("Hai messo una variabile nel modello, idiota!");
		return visitChildren(ctx);
	}
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitModel(BigraphParser.ModelContext ctx) {
		modelVisited = true;
		return visitChildren(ctx);
	}
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitProperty(BigraphParser.PropertyContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitProperty_statement(BigraphParser.Property_statementContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitBoolean_expression(BigraphParser.Boolean_expressionContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitBinary_operation(BigraphParser.Binary_operationContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitTerm(BigraphParser.TermContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitParameters_list(BigraphParser.Parameters_listContext ctx) { return visitChildren(ctx); }
	/**
	 * {@inheritDoc}
	 *
	 * <p>The default implementation returns the result of calling
	 * {@link #visitChildren} on {@code ctx}.</p>
	 */
	@Override public T visitParameter(BigraphParser.ParameterContext ctx) { return visitChildren(ctx); }
}